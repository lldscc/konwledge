/**
 * 一.常量Constant
 * */
// 1.const 声明常量
// 2.不能重新赋值
const  name = '张三'
const age = 20

console.log('name', name);
console.log('age', age);

console.log('name类型：',typeof name)
console.log('name类型：',typeof age) //类型

console.log('------------------')
/**
 * 二.变量Variable
 * */
// 1.let  声明变量
// 2.可以重新赋值
let address
address = '上海市'
console.log('address', address);

